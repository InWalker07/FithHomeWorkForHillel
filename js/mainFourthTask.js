var min = +prompt("Присвойте значение переменной min");
if (min >= 0 && min < 15) {
	alert("Переменная min попадает в первую четверть.");
}
if (min >= 15 && min < 30) {
	alert("Переменная min попадает во вторую четверть.");
}
if (min >= 30 && min < 45) {
	alert("Переменная min попадает в третью четверть.");
}
if (min >= 45 && min < 60) {
	alert("Переменная min попадает в четвертую четверть.");
}